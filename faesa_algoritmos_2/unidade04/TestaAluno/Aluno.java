
public class Aluno {
	private long matricula;
	private String nome;
	private String telefone;
	
	void setMatricula(long matricula)
	{
		this.matricula = matricula;
	}
	
	void setNome(String nome)
	{
		this.nome = nome;
	}
	
	void setTelefone(String telefone)
	{
		this.telefone = telefone;
	}
	
	String getTelefone()
	{
		return telefone;
	}

}
